  data() {
    return {
      angle: '0'
    }
  },
  computed: {
    // Get X-Cor on unit circle
    _x() {
      const r = 113.3937
      const offset = 170.48
      const rads = (this.angle * Math.PI) / 180
      return r * Math.cos(rads) + offset
    },
    // Get Y-Cor on unit circle
    _y() {
      const r = 113.3937
      const offset = 147.89
      const rads = (this.angle * Math.PI) / 180
      return -r * Math.sin(rads) + offset // negative as positive y axis is down in canvas
    },
    _xTxtLoc() {
      const r = 113.3937
      const offset = 170.48
      const rads = (this.angle * Math.PI) / 180
      const sf = 1.05
      return sf * r * Math.cos(rads) + offset
    },
    _yTxtLoc() {
      const r = 113.3937
      const offset = 147.89
      const rads = (this.angle * Math.PI) / 180
      const sf = 1.05
      return -sf * r * Math.sin(rads) + offset // negative as positive y axis is down in canvas
    },
    _xText() {
      const rads = (this.angle * Math.PI) / 180
      const value = Math.cos(rads).toFixed(2)
      return value === '-0.00' ? '0.00' : value
    },
    _yText() {
      const rads = (this.angle * Math.PI) / 180
      const value = Math.sin(rads).toFixed(2)
      return value === '-0.00' ? '0.00' : value
    },
    waveDashOffset() {
      const theta = this.angle
      let _tmp
      // Dash Offset is not linear with the anlge
      // These values are obtained by trial and error to match the sine wave perfectly

      // 0 - 90
      if (theta <= 50) _tmp = 200
      else if (theta <= 60) _tmp = 230
      else if (theta <= 65) _tmp = 250
      else if (theta <= 70) _tmp = 270
      else if (theta <= 79) _tmp = 290
      else if (theta <= 90) _tmp = 320

      // 90 - 180
      else if (theta <= 100) _tmp = 345
      else if (theta <= 110) _tmp = 360
      else if (theta <= 145) _tmp = 370
      else if (theta <= 145) _tmp = 370
      else if (theta <= 153) _tmp = 360
      else if (theta <= 165) _tmp = 350
      else if (theta <= 175) _tmp = 340
      else if (theta <= 190) _tmp = 330

      // 180 - 270
      else if (theta <= 195) _tmp = 320
      else if (theta <= 250) _tmp = 310
      else if (theta <= 255) _tmp = 315
      else if (theta <= 263) _tmp = 320
      else if (theta <= 270) _tmp = 325

