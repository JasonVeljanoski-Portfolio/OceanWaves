      else if (theta <= 330) _tmp = 345
      else if (theta <= 345) _tmp = 340
      else if (theta <= 355) _tmp = 335
      else if (theta <= 360) _tmp = 330

      // should not get here
      else _tmp = 330

      return this.mapVal(theta, 0, 360, 1000, _tmp)
    },
    horizontalX() {
      return this.mapVal(this.angle, 0, 360, 429.52, 896.84)
    }
  },
  methods: {
    mapVal(value, start1, stop1, start2, stop2) {
      return start2 + (value - start1) * (stop2 - start2) / (stop1 - start1)
    }
